package week1.day2;

public class FindOddNumbers {

	public static void main(String[] args) {
		int maxRange = 10;
       for (int i = 1; i<=maxRange; i++) {
    	   if(i%2 ==1) {
    		   System.out.println(i);  
    		   
    		   
    	   }
		
	}
       System.out.println("-----------------");
       int evenrange = 20;
       for (int j = 1; j <=evenrange; j++) {
		if (j%2 ==0) {
			System.out.print(j + " ");
		}
	}
	}
}
